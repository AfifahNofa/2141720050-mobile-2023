
void main() {
  for (int i = 18; i >= 9; i--) { 
        //int i = 18: Inisialisasi variabel i dengan nilai awal 18.
        // i >= 9: Kondisi untuk menjalankan perulangan, yaitu selama i lebih besar dari atau sama dengan 9.
        // i--: Ekspresi iterasi yang mengurangkan nilai i setiap kali loop berjalan.
        // Jadi, perulangan ini akan berjalan dari 18 hingga 9, mengurangkan nilai i seiring dengan setiap iterasi, hingga kondisi i >= 9 tidak lagi terpenuhi
    print('Nama saya Novita, sekarang berumur ${i}');
  }
}
